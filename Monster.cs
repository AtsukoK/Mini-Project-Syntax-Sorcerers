class Monster
{
    public int CurrentHitPoints;
    public int ID;
    public int MaximumDamage;
    public int MaximumHitPoints
    public string Name

    public Monster(int id, string name, int maximumdamage, int maximumhitpoints, int currenthitpoints)
    {
